void PrintEven(int iNo)
{
    int iCnt =0;

    if(iNo <= 0)
    {
        return;
    }

    for(iCnt =1;iCnt <= iNo *2;iCnt++)
    {
        if(iCnt % 2 == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}
////////////////////////////////////////////////////////////////////
//
// Entry point Function for the application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter the Number :");
    scanf("%d",&iValue);

    PrintEven(iValue);

    return 0;
}

