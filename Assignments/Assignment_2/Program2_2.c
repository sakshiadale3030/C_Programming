// Fuction Nmae :  Display
// Description :   It is used to print *
// Input :         Integer
// Output :        *
// Author :        Sakshi Ashok Adale
// Date :          20/10/2025
//
////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo > 0)
    {
        printf("*\t");
        iNo--;
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry point Function for the application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d",&iValue);

    Display(iValue);


    return 0;
}

