#include<stdio.h>

////////////////////////////////////////////////////////////////////
//
// Fuction Nmae :  Display
// Description :   It is used to print if number is less than 10
//                 then print "Hello" otherwise print "Demo"
// Input :         Integer
// Output :        Hello,Demo
// Author :        Sakshi Ashok Adale
// Date :          20/10/2025
//
////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
    
}

////////////////////////////////////////////////////////////////////
//
// Entry point Function for the application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d",&iValue);

    Display(iValue);


    return 0;
}

