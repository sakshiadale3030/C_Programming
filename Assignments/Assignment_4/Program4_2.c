int ReverseFact(int iNo)
{
    int iCnt = 0;
    
    for(iCnt = (iNo/2); iCnt >= 1 ;iCnt--)
    {
       if((iNo % iCnt) == 0)
        {
            printf("%d\t",iCnt);
        }
       
    }

}
////////////////////////////////////////////////////////////////////
//
// Entry point Function for the application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    ReverseFact(iValue);

    return 0;
}

