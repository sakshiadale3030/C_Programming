int MultiplicationFactor(int iNo)
{
    int iCnt = 0;
    int iFact = 1;
    
    for(iCnt = 1; iCnt <= (iNo/2);iCnt++)
    {
       if((iNo % iCnt) == 0)
        {
            iFact = iFact*iCnt;
        }
       
    }
     return iFact;

}
////////////////////////////////////////////////////////////////////
//
// Entry point Function for the application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0,iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = MultiplicationFactor(iValue);

    printf("Factors are : %d/n",iRet);

    return 0;
}

