int SummationNonFactor(int iNo)
{
    int iCnt = 0, iSum = 0;
    
    for(iCnt = 1 ; iCnt <= iNo ;iCnt++)
    {
       if((iNo % iCnt) != 0)
        {
           iSum = iSum + iCnt;
        }
    }
    return iSum;

}
